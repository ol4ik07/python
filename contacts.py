class contact:
    def __init__(self, firstname, midname, lastname, nik, title, company, address, home, mobile, work,
                    fax, email):
        self.firstname=firstname
        self.midname=midname
        self.lastname=lastname
        self.nik=nik
        self.title=title
        self.company=company
        self.address=address
        self.home=home
        self.mobile=mobile
        self.work=work
        self.fax=fax
        self.email=email

